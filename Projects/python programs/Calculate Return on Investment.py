# How to calculate Return on Investment? 

print("===]  Calculation Return on Investment   [===")

invest = int(input("How much you invest : "))
print("Total Investment : " , invest)

incomeFromInvestment = int(input("Income from Investment : "))
print("Income from Investment : " , incomeFromInvestment)

# Calculation ROI
ROI = (incomeFromInvestment - invest) / invest * 100/100
print(ROI*100, '%')
